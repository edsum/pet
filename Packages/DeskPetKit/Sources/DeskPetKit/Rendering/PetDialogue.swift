                            personality: PetPersonality = .lively) -> String {
        let pool = pools[mood] ?? [:]
        let byPerson = pool[personality] ?? pool[.lively] ?? ["…"]
        // 用 mood 做种子让同一 mood 同一段时间内稳定
        let seed = Int(Date().timeIntervalSince1970 / 60) + mood.rawValue.hashValue
        return byPerson[abs(seed) % byPerson.count]
    }

